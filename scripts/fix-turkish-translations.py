def main():
    data_dir = os.path.join(os.path.dirname(__file__), "..", "public", "data", "words")
    total_fixed = 0
    total_proper = 0
    total_unfixed = 0
    unfixed_counter = {}

    for fname in sorted(os.listdir(data_dir)):
        if not fname.endswith(".json"):
            continue
        fpath = os.path.join(data_dir, fname)
        with open(fpath, "r", encoding="utf-8") as f:
            data = json.load(f)

        changed = False
        for verse in data["verses"]:
            for word in verse["words"]:
                tr = word.get("translationTr", "")
                en = word.get("translation", "")
                if tr == en and tr:
                    # Ozel isimler zaten dogru
                    if en.strip() in PROPER_NOUNS:
                        total_proper += 1
                        continue
                    new_tr = translate_english_to_turkish(en)
                    if new_tr != en:
                        word["translationTr"] = new_tr
                        total_fixed += 1
                        changed = True
                    else:
                        total_unfixed += 1
                        unfixed_counter[en] = unfixed_counter.get(en, 0) + 1

        if changed:
            with open(fpath, "w", encoding="utf-8") as f:
                json.dump(data, f, ensure_ascii=False)

    print(f"Duzeltilen: {total_fixed}")
    print(f"Ozel isim (zaten dogru): {total_proper}")
