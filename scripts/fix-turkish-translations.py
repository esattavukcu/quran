def translate_by_pattern(en: str) -> str | None:
    """Bilinen kaliplara gore ceviri dener."""
    en_lower = en.strip().lower()

    # "(the) X" -> X'i DIRECT_MAP'te ara
    m = re.match(r'^\(the\)\s+(.+)$', en, re.IGNORECASE)
    if m:
        inner = m.group(1)
        # bazilarini dogrudan cevir
        inner_map = {
            "way": "yol", "punishment": "azap", "path": "yol",
            "people": "insanlar", "Day": "gun", "day": "gun",
            "Signs": "ayetler", "signs": "ayetler", "command": "emir",
            "creation": "yaratilis", "companions": "arkadaslar",
            "Children": "ogullari", "remembrance": "zikir",
            "news": "haber", "good things": "guzel seyler",
            "Lord": "Rab", "Book": "Kitap",
        }
        if inner in inner_map:
            return inner_map[inner]

    # "the X" -> sozlukte ara veya kelimeyi cevir
    if en_lower.startswith("the "):
        rest = en[4:]
        if rest in DIRECT_MAP:
            return DIRECT_MAP[rest]

    # "your X" kaliplari
    if en_lower.startswith("your "):
        rest = en[5:]
        base = DIRECT_MAP.get(rest) or DIRECT_MAP.get("the " + rest)
        if base:
            return base + " (senin)"

    # "their X" kaliplari
    if en_lower.startswith("their "):
        rest = en[6:]
        base = DIRECT_MAP.get(rest) or DIRECT_MAP.get("the " + rest)
        if base:
            return base + " (onlarin)"

    # "his X" kaliplari
    if en_lower.startswith("his "):
        rest = en[4:]
        base = DIRECT_MAP.get(rest) or DIRECT_MAP.get("the " + rest)
        if base:
            return base + " (onun)"

    # "her X" kaliplari
    if en_lower.startswith("her "):
        rest = en[4:]
        base = DIRECT_MAP.get(rest) or DIRECT_MAP.get("the " + rest)
        if base:
            return base + " (onun)"

    # "my X" kaliplari
    if en_lower.startswith("my "):
        rest = en[3:]
        base = DIRECT_MAP.get(rest) or DIRECT_MAP.get("the " + rest)
        if base:
            return base + " (benim)"

    # "before X" / "after X" kaliplari
    if en_lower.startswith("before "):
        rest = en[7:]
        base = DIRECT_MAP.get(rest)
        if base:
            return base + " once"
    if en_lower.startswith("after "):
        rest = en[6:]
        base = DIRECT_MAP.get(rest)
        if base:
            return base + " sonra"

    # "besides X"
    if en_lower.startswith("besides "):
        rest = en[8:]
        base = DIRECT_MAP.get(rest)
        if base:
            return base + " disinda"

    # "they X" fiil kaliplari
    if en_lower.startswith("they "):
        rest = en[5:]
