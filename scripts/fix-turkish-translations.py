def translate_english_to_turkish(en: str) -> str:
    """Ingilizce ifadeyi Turkceye cevirir."""
    en = en.strip()
    if not en:
        return en

    # 1. Dogrudan eslesme
    if en in DIRECT_MAP:
        return DIRECT_MAP[en]

    # 2. Genisletilmis sozluk
    if en in EXTENDED_MAP:
        return EXTENDED_MAP[en]

    # 3. Pattern tabanli ceviri
    pattern_result = translate_by_pattern(en)
    if pattern_result:
        return pattern_result

    # 4. Kucuk harf ile tekrar dene
    en_lower = en.lower().strip()
    for d in [DIRECT_MAP, EXTENDED_MAP]:
        for key, val in d.items():
            if key.lower() == en_lower:
                return val

    # 5. Bosluklu ifadeleri kelimelere bolup her birini cevir
    words = en.split()
    if len(words) > 1:
        translated_words = []
        for w in words:
            tw = DIRECT_MAP.get(w) or EXTENDED_MAP.get(w) or DIRECT_MAP.get(w.lower()) or EXTENDED_MAP.get(w.lower())
            translated_words.append(tw if tw else w)
        result = " ".join(translated_words)
        # En az bir kelime cevrildiyse kullan
        if result != en:
            return result

    # 6. Parantezli ifadeleri temizle ve tekrar dene
    cleaned = re.sub(r'\([^)]*\)\s*', '', en).strip()
    if cleaned and cleaned != en:
        result = translate_english_to_turkish(cleaned)
        if result != cleaned:
            return result

    # 7. Cevirilemeyen -> Ingilizce'yi dondur (degistirmeden birak)
    return en


